# Comparison operators
# equals                 ->  ==
# not equals             ->  !=
# greater than           ->  >
# less than              ->  <
# greater or equals than ->  >=
# lesser or equals than  ->  <=

# Example 1:
if "word" == "word"
  puts "it is"
else
  puts "it is not"
end

# Example 2:
sum    = (2 + 2) * 2
result = 4 * 2

if sum == result
  puts "they are equal"
else
  puts "they are not equal"
end
